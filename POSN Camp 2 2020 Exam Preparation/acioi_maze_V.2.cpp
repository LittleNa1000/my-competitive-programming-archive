#include<bits/stdc++.h>
using namespace std;//				F H S V
int d[]={INT_MAX-1,INT_MAX-1,INT_MAX-1,INT_MAX-1},mn;
short int n,m,y_i,y_j,idx;
bool visited[75][75],found_y;
char str[75][75];
void maze(short int i,short int j,int dist){
	if(str[i][j]=='F'){
		if(d[0]>dist) d[0]=dist;
	}
	else if(str[i][j]=='H'){
		if(d[1]>dist) d[1]=dist;
	}
	else if(str[i][j]=='S'){
		if(d[2]>dist) d[2]=dist;
	}
	else if(str[i][j]=='V'){
		if(d[3]>dist) d[3]=dist;
	}
	visited[i][j]=1;
	if(i>0&&str[i-1][j]!='#'&&!visited[i-1][j]) maze(i-1,j,dist+1);
	if(j<m-1&&str[i][j+1]!='#'&&!visited[i][j+1]) maze(i,j+1,dist+1);
	if(i<n-1&&str[i+1][j]!='#'&&!visited[i+1][j]) maze(i+1,j,dist+1);
	if(j>0&&str[i][j-1]!='#'&&!visited[i][j-1]) maze(i,j-1,dist+1);
}
main(){
	scanf("%hd %hd",&n,&m);
	for(int i=0;i<n;i++){
		cin>>str[i];
		if(!found_y){
			for(int j=0;j<m;j++){
				if(str[i][j]=='Y'){
					y_i=i;
					y_j=j;
					found_y=1;
					break;
				}
			}
		}
	}
	maze(y_i,y_j,0);
	//printf("%d %d %d %d",d[0],d[1],d[2],d[3]);
	for(short int i=0;i<4;i++){
		mn=INT_MAX;
		for(short int j=0;j<4;j++){
			if(mn>d[j]){
				mn=d[j];
				idx=j;
			}
		}
		if(idx==0) printf("F ");
		else if(idx==1) printf("H ");
		else if(idx==2) printf("S ");
		else printf("V ");
		d[idx]=INT_MAX;
	}
}
